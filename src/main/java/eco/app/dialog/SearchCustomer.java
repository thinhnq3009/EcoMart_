    private void addRow(DefaultTableModel model, Customer customer) {

        model.addRow(EntityHelper.getData(customer,
                "id",
                "fullname",
                "rank",
                "phone",
                "email"
        ));

    }

    public Customer getCustomerSelected() {
        return customerSelected;
    }

    private void init() {
        try {
            customers = new CustomerDao().getAll();

            DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();

            model.setRowCount(0);

            for (Customer customer : customers) {
                addRow(model, customer);
            }

        } catch (Exception e) {
            MessageHelper.showException(this, e);
        }

    }

