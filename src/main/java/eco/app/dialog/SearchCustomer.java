    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        int index = tblCustomer.getSelectedRow();
        int idCustomer = (int) tblCustomer.getValueAt(index, 0);
        customerSelected = (Customer) EntityHelper.find(customers, idCustomer);
        this.dispose();
    }//GEN-LAST:event_btnSelectActionPerformed

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        int index = tblCustomer.getSelectedRow();
        int idCustomer = (int) tblCustomer.getValueAt(index, 0);
        customerSelected = (Customer) EntityHelper.find(customers, idCustomer);
        if (evt.getClickCount() == 2) {
            dispose();
        }
    }//GEN-LAST:event_tblCustomerMouseClicked

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String key = txtSearch.getText().toLowerCase();
        
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
        
        model.setRowCount(0);
        
        for (Customer c : customers) {
            if (c.getFullname().toLowerCase().contains(key)
                    && cbFullname.isSelected()) {
                addRow(model, c);
            } else if ((c.getId() + "").contains(key)) {
                addRow(model, c);
            }
        }
        
    }//GEN-LAST:event_txtSearchKeyReleased

