    public void addButton(String text, MessageType mt) {
        ButtonRandius btn = new ButtonRandius(text);
        btn.setFont(btnHide.getFont());
        btn.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                messageType = mt;
                dispose();
            }

        });
        pnButton.add(btn, 0);
    }

