    private void tblEmployeesMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tblEmployeesMouseClicked
        if (evt.getClickCount() == 2) {
            int indexSelected = tblEmployees.getSelectedRow();

            if (indexSelected == -1)
                return;

            idEmployeeSelected = (int) tblEmployees.getValueAt(indexSelected, 0);

            Employee employee = (Employee) EntityHelper.find(employees, idEmployeeSelected);

            fillForm(employee);

        }
    }

