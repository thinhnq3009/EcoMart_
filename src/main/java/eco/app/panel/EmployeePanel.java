    @Override
    public void addTableRow(Employee emp) {
        tblModel.addRow(EntityHelper.getData(emp,

                "id",
                "fullname",
                "phone",
                "email",
                "soldOut",
                "salary"

        ));
    }

    @Override
    public void fillTable() {
        try {
            tblModel.setRowCount(0);

            employees = new EmployeeDao().getAll();

            for (Employee employee : employees) {
                addTableRow(employee);
            }
        } catch (Exception e) {
            MessageHelper.showException(this, e);
        }
    }

