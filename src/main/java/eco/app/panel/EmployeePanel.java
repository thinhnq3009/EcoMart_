    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {
        try {

            StringBuilder sb = new StringBuilder();

            Employee employee = readForm(sb);

            if (employee == null) {
                return;
            }

            EmployeeDao dao = new EmployeeDao();

            if (dao.insert(employee)) {
                MessageHelper.showMessage(this, "Insert new employee successfully!");
                clearForm();
            } else {
                MessageHelper.showErrorMessage(this, "Insert new employee failed!");
            }

        } catch (Exception e) {
            MessageHelper.showException(this, e);
        } finally {
            fillTable();
        }

    }

