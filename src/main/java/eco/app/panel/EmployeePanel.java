    @Override
    public boolean validateForm(StringBuilder sb) {

        if (sb == null) {
            sb = new StringBuilder();
        }

        txtFullname.check(sb, "Fullname is empty.\n");
        txtUsername.check(sb, "Username is empty.\n");
        txtPassword.check(sb, "Password is empty.\n");
        txtAddress.check(sb, "Address is empty.\n");
        txtPhone.check(sb, "Phone is empty.\n");
        txtEmail.check(sb, "Email is empty.\n");

        return sb.isEmpty();

    }

    @Override
    public void clearForm() {
        txtFullname.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        txtAddress.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtNote.setText("");
        rdoMale.setSelected(true);

        nav.isCreateMode();

        idEmployeeSelected = -1;
    }

    @Override
    public void fillForm(Employee employee) {
        idEmployeeSelected = employee.getId();
        txtFullname.setText(employee.getFullname());
        txtUsername.setText(employee.getUsername());
        txtPassword.setText(employee.getPassword());
        txtAddress.setText(employee.getAddress());
        txtPhone.setText(employee.getPhone());
        txtEmail.setText(employee.getEmail());
        txtNote.setText(employee.getNote());
        if (employee.isGender()) {
            rdoMale.setSelected(true);
        } else {
            rdoFemale.setSelected(true);
        }

        nav.isEditMode();
    }

    @Override
    public Employee readForm(StringBuilder sb) throws Exception {
        if (!validateForm(sb)) {
            MessageHelper.showErrorMessage(this, sb.toString());
            return null;
        }
        Employee employee = new Employee();

        employee.setId(idEmployeeSelected);
        employee.setFullname(txtFullname.getText());
        employee.setUsername(txtUsername.getText());
        employee.setPassword(txtPassword.getText());
        employee.setAddress(txtAddress.getText());
        employee.setPhone(txtPhone.getText());
        employee.setEmail(txtEmail.getText());
        employee.setNote(txtNote.getText());
        employee.setGender(rdoMale.isSelected());

        return employee;
    }
